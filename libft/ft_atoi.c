#include "libft.h"

int		ft_atoi(const char *s)
{
	int i;
	int ok;
	int n;

	n = 0;
	i = 0;
	ok = 0;
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t' || s[i] == '\v'
			|| s[i] == '\f' || s[i] == '\r')
		i++;
	if (s[i] == '-')
		ok = 1;
	if (s[i] == '-' || s[i] == '+')
		i++;
	while (s[i] >= '0' && s[i] <= '9' && s[i])
	{
		n *= 10;
		n += s[i] - '0';
		i++;
	}
	if (ok)
		return (-n);
	else
		return (n);
}
