#include "libft.h"

void	ft_putnbr(int c)
{
	if (c == -2147483648)
		ft_putstr("-2147483648");
	if (c < 0 && c > -2147483648)
	{
		ft_putchar('-');
		c = c * (-1);
	}
	if (c < 10 && c > -2147483648)
		ft_putchar(c + 48);
	else if (c >= 10)
	{
		ft_putnbr(c / 10);
		ft_putnbr(c % 10);
	}
}
