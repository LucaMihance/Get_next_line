#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	int		j;
	char	*str;

	j = ft_strlen(s);
	str = (char*)malloc(sizeof(char) * j + 1);
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
